

def calculate_sum(number_1:int|float, number_2:int|float)-> int|float:
    return number_1 + number_2

print(calculate_sum(10, 20))
print(calculate_sum(11, -23))
print(calculate_sum(10, 23))


def calculate_sub(number_1:int|float, number_2:int|float)-> int|float:
    return number_1 - number_2


def calculate_mul(number_1:int|float, number_2:int|float)-> int|float:
    return number_1 * number_2


def calculate_div(number_1:int|float, number_2:int|float)-> int|float:
    return number_1 / number_2